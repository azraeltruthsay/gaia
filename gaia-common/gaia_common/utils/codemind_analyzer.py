def trace_related_files(detection: Dict[str, Any]) -> List[str]:
    """Given a detection, find likely related source files for root cause analysis.

    Uses keyword matching against the TRACE_MAP and the detection's file_path
    to find files that might contain the root cause.
    """
    related = set()
    description = detection.get("description", "").lower()
    issue_type = detection.get("issue_type", "").lower()
    file_path = detection.get("file_path", "")

    # Add the detection's own file
    if file_path and os.path.isfile(file_path):
        related.add(file_path)

    # Keyword-based tracing
    search_text = f"{description} {issue_type}"
    for keyword, files in TRACE_MAP.items():
        if keyword in search_text:
            for fp in files:
                if os.path.isfile(fp):
                    related.add(fp)

    return sorted(related)[:6]  # Cap at 6 files to stay within token budget
